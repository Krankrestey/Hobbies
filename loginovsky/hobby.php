	<h2>Немного истории гитары</h2>
	<p>Самые ранние сохранившиеся свидетельства о струнных инструментах , предках современной гитары,<img src="images/tar.jpg" align="right" hspace="15"/> относятся ко II тысячелетию до н. э.Изображения киннора (шумеро-вавилонский струнный инструмент, упоминается в Библии) найдены на глиняных барельефах при археологических раскопках в Месопотамии. В Древнем Египте и Индии также были известны похожие инструменты: набла, нефер, цитра в Египте, вина и ситар в Индии.</p>
	<p>В Средние века основным центром развития гитары была Испания, куда гитара попала из древнего Рима (латинская гитара) и вместе с арабскими завоевателями (мавританская гитара). В XV веке получает распространение изобретённая в Испании гитара с 5 сдвоенными струнами (первая струна могла быть и одиночной). Такие гитары получают название испанских гитар. К концу XVIII века испанская гитара в процессе эволюции приобретает 6 одиночных струн</p>
	<p>В течение XVIII.XIX веков конструкция испанской гитары претерпевает значительные изменения, мастера экспериментируют с размером и формой корпуса, креплением грифа, конструкцией колкового механизма и прочим. Наконец, в XIX веке испанский гитарный мастер Антонио Торрес придаёт гитаре современные форму и размер. Гитары конструкции Торреса сегодня называют классическими. Наиболее известным гитаристом того времени является испанский композитор и гитарист Франсиско Таррега, заложивший основы классической техники игры на гитаре. В XX веке его дело продолжил испанский композитор, гитарист и педагог Андрес Сеговия.</p><br><br>	
