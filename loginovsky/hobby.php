	<p>В Средние века основным центром развития гитары была Испания, куда гитара попала из древнего Рима (латинская гитара) и вместе с арабскими завоевателями (мавританская гитара). В XV веке получает распространение изобретённая в Испании гитара с 5 сдвоенными струнами (первая струна могла быть и одиночной). Такие гитары получают название испанских гитар. К концу XVIII века испанская гитара в процессе эволюции приобретает 6 одиночных струн</p>
